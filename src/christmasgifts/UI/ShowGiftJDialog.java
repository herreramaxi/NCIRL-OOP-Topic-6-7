    public void showGift(ChristmasGift gift) {
        jTextFieldRecipient.setText(gift.getRecipient());
        jTextFieldIdea.setText(gift.getIdea());
        jTextFieldURL.setText(gift.getUrl());
        jTextFieldURL.setCaretPosition(0);
        this.setVisible(true);
    }

