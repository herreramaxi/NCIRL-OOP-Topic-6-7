    private void jTextFieldURLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextFieldURLMouseClicked
        try {
            //            Desktop.getDesktop().browse(new URI(jTextFieldURL.getText()));
            Desktop.getDesktop().browse(new URI(jTextFieldURL.getText()).normalize());

        } catch (Exception ex) {
            Logger.getLogger(ShowGiftJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextFieldURLMouseClicked

    //I took the idea from below link:
    //@Reference: https://stackoverflow.com/questions/24702434/copy-text-to-clipboard-from-a-jtextfield-with-press-of-a-button
    private void jButtonCopyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCopyMouseClicked
        try {
            StringSelection stringSelection = new StringSelection(jTextFieldURL.getText());
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(stringSelection, null);
        } catch (Exception ex) {
            Logger.getLogger(ShowGiftJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonCopyMouseClicked

